        public void buscarTableId(int medicoId){
        DefaultTableModel modelo = (DefaultTableModel) jTableMedicosRegistrados.getModel();
        modelo.setNumRows(0);
        MedicoDAO mdao = new MedicoDAO();
        Medico m = new Medico();
        try{
        m = mdao.getById(medicoId);
        modelo.addRow(new Object[]{
            m.getMedicoId(),
            m.getNomeCompleto(),
            m.getNascimento(),
            m.getSexo(),
            m.getIdentidade(),
            m.getCPF(),
            m.getEspecializacao().getDesc(),
            m.getCidade().getNome(),
            
        });

        }catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Falha ao preencher a tabela: " + ex);
            limparTable();
        }
    }
    
         public void buscarTable(){
         
         DefaultTableModel modelo = (DefaultTableModel) jTableMedicosRegistrados.getModel();
         modelo.setNumRows(0);
         MedicoDAO mdao = new MedicoDAO();
         List<Medico> medicos = new ArrayList<>();
         medicos = mdao.getAll();

         try {
             for(Medico m : medicos){
                 modelo.addRow(new Object[]{
                 m.getMedicoId(),
                 m.getNomeCompleto(),
                 m.getNascimento(),
                 m.getSexo(),
                 m.getIdentidade(),
                 m.getCPF(),
                 m.getEspecializacao().getDesc(),
                 m.getCidade().getNome(),
                 }
                 );
                           
             }
         } catch (Exception ex) {
             JOptionPane.showMessageDialog(null,"Falha ao preencher a Table" + ex);
             limparTable();
         }
     }
         

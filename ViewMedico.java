    private void pesquisarPorIdBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesquisarPorIdBtnActionPerformed
        Medico m = new Medico();
        
        MedicoDAO mdao = new MedicoDAO();
        
        
        m = mdao.getById(Integer.parseInt(txtIdMedico.getText()));
        
        txtNome.setText(m.getNomeCompleto());
        txtNascimento.setText(m.getNascimento());
        txtIdentidade.setText(m.getIdentidade());
        txtCPF.setText(m.getCPF());
        
        
        buscarTableId((int)m.getMedicoId());
    }//GEN-LAST:event_pesquisarPorIdBtnActionPerformed

    private void alterarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarBtnActionPerformed
        Medico m = new Medico();
        MedicoDAO edao = new MedicoDAO();
        Especializacao e = new Especializacao();
        Cidade c = new Cidade();
        
        String sexo;
        
        m.setMedicoId(Long.parseLong(txtIdMedico.getText()));
        m.setNomeCompleto(txtNome.getText());
        m.setNascimento(txtNascimento.getText());
        if(jCheckBox1.isSelected()){
            m.setSexo("Masculino");
        }
        else if(jCheckBox2.isSelected()){
            m.setSexo("Feminino");
        }
        sexo = m.getSexo();
        m.setIdentidade(txtIdentidade.getText());
        m.setCPF(txtCPF.getText());
        e = (Especializacao) especializacaoCombo.getSelectedItem();
        c = (Cidade) cidadeCombo.getSelectedItem();
        m.setCidade(c);
        
        m.setEspecializacao(e);
        
        
        edao.update(m);
        buscarTableId((int)m.getMedicoId());
    }//GEN-LAST:event_alterarBtnActionPerformed

    private void removerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerBtnActionPerformed
        Medico m = new Medico();
        MedicoDAO mdao = new MedicoDAO();
        
        m = mdao.getById(Integer.parseInt(txtIdMedico.getText()));
        
        mdao.remove((int) m.getMedicoId());
    }//GEN-LAST:event_removerBtnActionPerformed


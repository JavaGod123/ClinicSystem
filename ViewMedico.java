    private void cadastrarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarBtnActionPerformed
        Medico m = new Medico();
        Especializacao e = new Especializacao();
        Cidade c = new Cidade();
        MedicoDAO mdao = new MedicoDAO();
        controlMedico CM = new controlMedico();
        
        
        m.setNomeCompleto(txtNome.getText());
        m.setNascimento(sqldate);
        if(jCheckBox1.isSelected()){
            m.setSexo("Masculino");
        }
        else if(jCheckBox2.isSelected()){
            m.setSexo("Feminino");
        }
        sexo = m.getSexo();
        m.setIdentidade(txtIdentidade.getText());
        m.setCPF(txtCPF.getText());
        e = (Especializacao) especializacaoCombo.getSelectedItem();
        c = (Cidade) cidadeCombo.getSelectedItem();
        
        m.setEspecializacao(e);
        m.setCidade(c);
        
        CM.addMedico(e, c, txtNome.getText(), txtNascimento.getText(), sexo, txtIdentidade.getText(), txtCPF.getText());
        
        buscarTable();
        
    }//GEN-LAST:event_cadastrarBtnActionPerformed

